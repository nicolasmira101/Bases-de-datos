    private int obtenerSillasDisponibles(List<Sillaporestadio> listaSillas, String idPartido) {

        int d = 0, suma1 = 0, suma2 = 0, suma3 = 0, suma4 = 0;
        List<Categoria> lcat;
        lcat = controllerCategoria.findCategoriaEntities();

        for (Sillaporestadio spe : listaSillas) {
            if (spe.getSilla().getDisponible().equals("si")) {
                for (Categoria c : lcat) {
                    if (Objects.equals(spe.getSilla().getIdcategoria().getIdcategoria(), c.getIdcategoria())) {

                        System.out.println(spe.getSilla().getIdcategoria().getIdcategoria());
                        if (c.getIdcategoria() == 1) {
                            suma1++;
                        }
                        if (c.getIdcategoria() == 2) {
                            suma2++;
                        }
                        if (c.getIdcategoria() == 3) {
                            suma3++;
                        }
                        if (c.getIdcategoria() == 4) {
                            suma4++;
                        }
                    }
                }
                d++;
            }
        }

        List<Integer> lsum = new ArrayList<>();
        lsum.add(suma1);
        lsum.add(suma2);
        lsum.add(suma3);
        lsum.add(suma4);

        List<Categoriaporpartido> listacat;
        List<Categoriaporpartido> categorias = new ArrayList<>();
        listacat = controladorcatpar.findCategoriaporpartidoEntities();

        DefaultTableModel modelo = (DefaultTableModel) jTableDisponibilidadEntradasCategorias.getModel();
        Object[] fila = new Object[(modelo.getColumnCount())];

        for (int i = 0; i < listacat.size(); i++) {
            if (listacat.get(i).getPartido().getIdpartido() == Integer.parseInt(idPartido)) {
                categorias.add(listacat.get(i));

            }
        }
        for (int i = 0; i < categorias.size(); i++) {
            fila[0] = listacat.get(i).getCategoria().getIdcategoria();
            fila[1] = listacat.get(i).getPrecio();
            fila[2] = lsum.get(i);
            modelo.addRow(fila);
        }

        return d;
    }

