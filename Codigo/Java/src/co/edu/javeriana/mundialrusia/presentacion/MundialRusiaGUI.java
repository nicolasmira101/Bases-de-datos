    private void jButtonAddAnotacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddAnotacionActionPerformed

        minutoAnotacion = Integer.parseInt(jTextFieldMinuto.getText());
        if (minutoAnotacion < 0 || minutoAnotacion > 120) {
            JOptionPane.showMessageDialog(null, "El minuto no esta en el rango", "Error minuto", JOptionPane.ERROR_MESSAGE);
        } else {
            jComboBoxEquipo.removeAllItems();
            jComboBoxJugador.removeAllItems();
            jTextFieldMinuto.setText("");
            agregarAnotacionMarcador(equipoQueAnoto);
            agregarDetallesAnotacion(equipoQueAnoto, jugadorQueAnoto, minutoAnotacion);
            agregarTipoTarjeta();
            getTabbedPane().setSelectedIndex(2);
        }
    }//GEN-LAST:event_jButtonAddAnotacionActionPerformed

