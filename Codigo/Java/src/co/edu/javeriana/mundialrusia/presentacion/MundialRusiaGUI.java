    void cargarDatosUsuario(String idPartido, String categoria, String totalPagar, String estadio, String fila, String silla)
    {
        
        String nombre = jTextFieldNombre.getText();
        String apellido = jTextFieldApellido.getText();
        String pasaporteString = jTextFieldPasaporte.getText();
        
        List<Cliente> lc;
        ClienteJpaController contcli = new ClienteJpaController();
        lc = contcli.findClienteEntities();
        for (Cliente c : lc) {
            int pasaporte = Integer.parseInt(pasaporteString);
            if (c.getNombre().equals(jTextFieldNombre.getText()) && c.getApellido().equals(jTextFieldApellido.getText()) && c.getPasaporte() == pasaporte) {
                JOptionPane.showMessageDialog(null, "Usuario valido");
                confirmarPago(idPartido, categoria, totalPagar, estadio, fila, silla, nombre, apellido, pasaporteString);
                //ConfirmaPago cp= new ConfirmaPago(this.idp,this.cat, this.precio, this.estadio,fila, silla, txtnombre.getText(),jTextField2.getText(), jTextField3.getText());
            } else if (!(c.getNombre().equals(jTextFieldNombre.getText())) || !(c.getApellido().equals(jTextFieldApellido.getText())) || !(c.getPasaporte() == pasaporte)) {
                JOptionPane.showMessageDialog(null, "Usuario no valido");
            }
        }
    }
    
    void confirmarPago(String idPartido, String categoria, String totalPagar, String estadio, String fila, String silla, String nombre, String apellido, String pasaporteString)
    {
        
        int numeroFactura = 1000;
        java.util.Date fechaActual = new Date();
        
        jLabelNombres.setText(nombre);
        jLabelApellidos.setText(apellido);
        jLabelDocumento.setText(pasaporteString);
        
        jLabelNombreEstadio.setText(estadio);
        jLabelNumPartido.setText(idPartido);
        //jLabelFechaHora.setText();
        //jLabelEquipos.setText();
        
        //jLabelNumeroEntrada.setText();
        jLabelCategoriaEntrada.setText(categoria);
        jLabelSillas.setText(silla + fila);
        
        jLabelTotalPagarConfirmacion.setText(totalPagar);
        //jLabelFormaPago.setText();
        //jLabelRecibido.setText();
        jLabelNumeroFactura.setText(String.valueOf(numeroFactura));
        numeroFactura++;
        
        jLabelFechaCompra.setText(fechaActual.getDay() + "-" + fechaActual.getMonth() + "-" + fechaActual.getYear() + " " + fechaActual.getHours() + ":" + fechaActual.getMinutes());
    
        getTabbedPane().setSelectedIndex(12);
    }


